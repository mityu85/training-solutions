package week10d02;

public class Stop {

    private int number;
    private int numberOfPersons;

    public Stop(int number, int numberOfPersons) {
        this.number = number;
        this.numberOfPersons = numberOfPersons;
    }

    public int getNumber() {
        return number;
    }

    public int getNumberOfPersons() {
        return numberOfPersons;
    }
}
